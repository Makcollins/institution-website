                    <hr><h2> This is Our Patron</h2><hr>
                    <p>St. Anselm came from a noble Lombard family and was born in Aosta in the Italian Alps. His mother was a good and pious Burgundian woman, but after her death, his father’s violence and harshness finally caused Anselm to flee to France. Here after several years of wandering he took the Benedictine habit in 1060 at Bec (Normandy), where his illustrious countryman Blessed Lanfranc had started the famous school and soon he was outstanding for his learning. Within a span of three years, he was made Prior and after another 15 years, despite his reluctance, Abbot.</p>
                    <br><p>As Abbot St. Anselm had to cross over to England from time to time in connection with his abbey’s English properties, and there he became known and highly esteemed for his virtues and fearless zeal. So much so that in 1099 he was made Archbishop of Canterbury in the hope that he would be able cope with the encroachments of King William, the Red.</p>
                    <br><p>Gentle and self-effacing by nature, St. Anselm was unyielding on matters of principle. A man of fearless zeal, he strongly denounced the slave trade and staunchly defended the Filioque (“and from the Son”), a doctrine that indicated that the Holy Spirit proceeded from the Father and the Son.</p>
                    <br><p>Many important writings of this gentle Prelate, who was considered the greatest intellectual of his age and a ‘pattern for all theologians’, were composed during his two banishments. They earned him the title of ‘Father of Scholasticism’ and ‘Doctor of the Universal Church’ (1720) on account of the deep and lasting influence which his thinking has had upon philosophy and theology.</p>
