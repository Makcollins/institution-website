            <strong>STUDIES</strong>

            <p>The Scholastic Year in our School is from the month of April to March. The school is preparing students for the All India Secondary and Senior Secondary Certificate Examination of the Central Board of Secondary Education, New Delhi and will admit students of all communities, treating their religious beliefs with due respect. The following subjects are taught in the Senior Secondary classes: Moral Science, English. Hindi, Physics, Chemistry, Biology, Mathematics, Geography, Economics, Political Science, Business Studies, Accountancy, Computer Science, Informatics Practices and Sociology.

              As education is a gradual and graded process and a strong and correct foundation is the basis of future success, the school caters to the need of children of age 3½ years and above.

              The School provides a sound education designated to fit its students for the world of tomorrow, in whatever sphere of activity they may be called to; an education in the fullest sense is offered for the moral, intellectual, social and physical development.</p>
