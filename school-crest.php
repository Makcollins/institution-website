          <div class="card-text">

          <p> St. Anselm: The Central Figure is of St. Anselm,the Patron of the school. He is shown in the attire of a bishop with an open book in his hands. St. Anselm besides being one of the most illustrious Archbishops to occupy the See of Canterbury was a renowned Philosopher and Doctor of the Church keenly interested in the Education of Youth.</p>
          <p> The Franciscan Coat-Of-arms: To the right of St. Anselm, you find in the Franciscan Coat-of-arms i.e. the hand of Christ crosses the hand of Francis, with the cross standing between. The emblem is to remind us that the Institution was Founded by the Franciscan Fathers of the Capuchin Order and of the traditions they have established.</p>
          <p> The Ashoka Wheel:  On the left of St. Anselm lies the Ashoka Wheel, denoting that with the advent of Independence the College had been entrusted to the sons of the soil, the Secular Clergy.</p>
          <p> Our Lady:  The top portion of the Shield bears the image of the Blessed Virgin Mary (Immaculate Conception), denoted by the letters I and C on either side. The picture is a reproduction of the beautiful stain window of the adjoining Cathedral, under the shadow of which the Institution has grown to its present stature. The background of deep blue,denoted by the horizontal lines together with the white of the lower half form the School Colours - Blue and White.</p>
          <p> The Supporting Animals: The shield is supported by a unicorn on the right and a lion on the left to remind us of the singleness of purpose in achieving our ideals and the Courage to live up to them.</p>
          <p> The Motto: Beneath runs the School Motto:  "Deo Amabiles et Hominibus". It means pleasing to God and to men. Such is also the aim and purpose of St. Anselm's.</p>
          <p> The Star: The star surmounting the crest reminds us that the school is situated at the foot of the mighty "Taragarh" the "Fort of the Star", the famous "Garh Beetli" of Rajput poems and closely connected with the immortal Prithvi Raj. The mountain raises its rugged  summit rigid above the School. The star also suggests to us that  star which long ago led the Kings  of the East to the Knowledge of the one true God. May St. Anselm's school serve the same purpose.</p>
